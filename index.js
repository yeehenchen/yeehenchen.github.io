function toggleInfo() {
  const info = document.querySelector('#info-content');
  const work = document.querySelector('#work-content');
  info.style.display = 'block';
  work.style.display = 'none';
}

function toggleWork() {
  const info = document.querySelector('#info-content');
  const work = document.querySelector('#work-content');
  info.style.display = 'none';
  work.style.display = 'block';
}


